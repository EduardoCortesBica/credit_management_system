CREATE TABLE CATEGORIA (
    ID_Categoria INTEGER PRIMARY KEY AUTOINCREMENT,
    Nome VARCHAR(50) NOT NULL
);

CREATE TABLE SERVICO (
    ID_Servico INTEGER PRIMARY KEY AUTOINCREMENT,
    Nome VARCHAR(50) NOT NULL
);

CREATE TABLE STATUS_ATENDIMENTO (
    ID_Status_Atendimento INTEGER PRIMARY KEY AUTOINCREMENT,
    Descricao VARCHAR(50) NOT NULL
);

CREATE TABLE VENDEDOR (
    ID_Vendedor INTEGER PRIMARY KEY AUTOINCREMENT,
    Nome VARCHAR(100) NOT NULL,
    Login VARCHAR(50) NOT NULL
);

CREATE TABLE CLIENTE (
    ID_Cliente INTEGER PRIMARY KEY AUTOINCREMENT,
    Nome VARCHAR(100) NOT NULL,
    Telefone VARCHAR(20),
    Matricula VARCHAR(50),
    Email VARCHAR(100),
    Renda DECIMAL(10, 2),
    ID_Categoria INTEGER,
    FOREIGN KEY (ID_Categoria) REFERENCES CATEGORIA(ID_Categoria)
);

CREATE TABLE ATENDIMENTO (
    ID_Atendimento INTEGER PRIMARY KEY AUTOINCREMENT,
    Data_Hora DATETIME NOT NULL,
    Observacao TEXT,
    ID_Cliente INTEGER,
    ID_Vendedor INTEGER,
    ID_Servico INTEGER,
    ID_Status_Atendimento INTEGER,
    FOREIGN KEY (ID_Cliente) REFERENCES CLIENTE(ID_Cliente),
    FOREIGN KEY (ID_Vendedor) REFERENCES VENDEDOR(ID_Vendedor),
    FOREIGN KEY (ID_Servico) REFERENCES SERVICO(ID_Servico),
    FOREIGN KEY (ID_Status_Atendimento) REFERENCES STATUS_ATENDIMENTO(ID_Status_Atendimento)
);